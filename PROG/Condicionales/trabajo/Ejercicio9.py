if diasemana <=0 or diasemana >7:
    print ('Día no disponible')

if diasemana==1:
    print ('Lunes')
    
elif diasemana==2:
    print ('Martes')

elif diasemana==3:
    print ('Miércoles')

elif diasemana==4:
    print ('Jueves')

elif diasemana==5:
    print ('Viernes')

elif diasemana==6:
    print ('Sábadp')

elif diasemana==7:
    print ('Domingo')
